    ios::sync_with_stdio(false);

    int n, m; cin>>n>>m;
    vector<vector<int>> G(n);
    for(int i=0; i<m; i++){
        int a, b; cin>>a>>b;
        a--, b--;
        G[a].emplace_back(b);
        G[b].emplace_back(a);
    }

    queue<int> que;
    que.emplace(0);
    vector<bool> visited(n, false);
    visited[0] = true;
    vector<int> ans(n, -1);
    while(que.size()){
        int cv = que.front(); que.pop();
        for(auto nv:G[cv]){
            if(visited[nv]) continue;

            visited[nv] = true;
            ans[nv] = cv;
            que.emplace(nv);
        }
    }

    cout << "Yes" << endl;
    for(int i=1; i<n; i++){
        cout << ans[i] + 1 << endl;
    }

    return 0;
