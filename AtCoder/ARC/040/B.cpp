    int n, r; cin>>n>>r;
    string s; cin>>s;

    int ans = 0;
    for(int i=n-r; i>=0; i--){
        if(s[i+r-1] == '.'){
            if(ans == 0) ans += i;
            for(int j=i; j<i+r; j++){
                s[j] = 'o';
            }
            ans++;
        }
    }

    bool exist = false;
    for(int i=0; i<r; i++){
        if(s[i] == '.') exist = true;
    }

    cout << ans + exist << endl;

    return 0;
